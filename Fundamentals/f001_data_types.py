# The `type()` function returns the type of the object passed as an argument.

# Example 1: Type of a string
print(type("Hello world!"))  # Output: <class 'str'>
# 'Hello world!' is a string, represented in Python as 'str'. 
# Strings are an immutable collection of characters.

# Example 2: Type of an integer
print(type(5))  # Output: <class 'int'>
# 5 is an integer, represented in Python as 'int'. 
# Integers can be positive, negative, or zero.

# Example 3: Type of a floating-point number
print(type(5.5))  # Output: <class 'float'>
# 5.5 is a number with a decimal part, known as a floating-point number ('float').

# Example 4: Type of a boolean value
print(type(True))  # Output: <class 'bool'>
# True is a boolean value ('bool'), one of the two logical values in Python (True or False).

# Example 5: Type of a dictionary
print(type({'animal':'dog'}))  # Output: <class 'dict'>
# {'animal': 'dog'} is a dictionary, represented in Python as 'dict'.
# It is a collection of key-value pairs, where 'animal' is the key and 'dog' is the value.

# Example 6: Type of a list
print(type([1, 2, 3]))  # Output: <class 'list'>
# [1, 2, 3] is a list, represented in Python as 'list'. 
# It is an ordered collection of elements that can be modified (mutable).

# Example 7: Type of a tuple
print(type((1, 2, 3)))  # Output: <class 'tuple'>
# (1, 2, 3) is a tuple, represented in Python as 'tuple'.
# It is an ordered collection of elements that cannot be modified (immutable).

# Example 8: Type of a set
print(type({1, 2, 3}))  # Output: <class 'set'>
# {1, 2, 3} is a set, represented in Python as 'set'.
# It is an unordered collection of unique elements.

# Example 9: Type of a complex number
print(type(1 + 3j))  # Output: <class 'complex'>
# 1 + 3j is a complex number, represented in Python as 'complex'.
# It has a real part (1) and an imaginary part (3j).
